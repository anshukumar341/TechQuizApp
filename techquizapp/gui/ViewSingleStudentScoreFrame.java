    private void jcStudentItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcStudentItemStateChanged
       
      if(evt.getStateChange()==ItemEvent.DESELECTED)
          return;
        String select=jcStudent.getSelectedItem().toString();
        try
        {
            
   
        ArrayList<String> examid=PerformanceDao.getAllExamId(select);
    //   if(jcStudent.se)
        jcExamId.removeAllItems();
        for(String exam:examid)
        {
        jcExamId.addItem(exam);
        }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "exception in jcStudent","Exception",JOptionPane.ERROR_MESSAGE);
        }
      
        
    }//GEN-LAST:event_jcStudentItemStateChanged

    private void jcExamIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcExamIdItemStateChanged
        if(evt.getStateChange()==ItemEvent.DESELECTED)
            return;
        
        String userid=jcStudent.getSelectedItem().toString();
        String examid=jcExamId.getSelectedItem().toString();
        try
        {
            StudentScore score=PerformanceDao.getStudentDetails(userid, examid);
            txtPercentage.setText(String.valueOf(score.getPer()));
            txtLanguage.setText(score.getLanguage());
            txtPercentage.setEditable(false);
            txtLanguage.setEditable(false);
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Exception in ExamId Combo", "Exception", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jcExamIdItemStateChanged

