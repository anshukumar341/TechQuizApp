    public void loadStudentId()
    {
        ArrayList<String> details=null;
        try
        {
            jcStudent.removeAllItems();
            details=PerformanceDao.getAllStudentId();
           if(details.isEmpty()==true)
            {
                JOptionPane.showMessageDialog(null, "No Record Found!!","No Record",JOptionPane.ERROR_MESSAGE);
               return;
           }
            for(String data:details)
            {
                jcStudent.addItem(data);
                
            }
                
            
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Exception during data base Communication","Exception",JOptionPane.ERROR_MESSAGE);
        }
    }
           


