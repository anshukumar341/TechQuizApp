    public int validateInput()
    {
       userId=txtUserId.getText();
       char []pwd1=txtPassword.getPassword();
       char []pwd2=txtRetype.getPassword();
       password=String.valueOf(pwd1);
       String retype=String.valueOf(pwd2);
       if(userId.isEmpty()||pwd1.length==0||pwd2.length==0)
           return 0;
       else if(userId.length()<4&&pwd1.length<4&&pwd2.length<4)
           return -1;
       else if(!password.equals(retype))
           return -2;
       else
           return 1;
       
    }
    
