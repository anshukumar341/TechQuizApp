    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        int ans=validateInput();
        if(ans==0)
        {
            JOptionPane.showMessageDialog(null, "please dont't left input field!", "error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else if(ans==-1)
        {
            JOptionPane.showMessageDialog(null, "user id and password must be greater than 4 character!","Error",JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        else if(ans==-2)
        {
            JOptionPane.showMessageDialog(null, "password and retype password are not same", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        else
        {
            UserPojo user=new UserPojo();
            user.setUserId(userId);
            user.setPassword(password);
            user.setUserType("Student");
            try
            {
                boolean result=UserDao.addUser(user);
                if(result)
                {
                    JOptionPane.showMessageDialog(null, "Registration complete!","Success",JOptionPane.INFORMATION_MESSAGE);
                    clearall();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "UserId already present!","Error",JOptionPane.ERROR_MESSAGE);
                }
            }
            catch(SQLException ex)
            {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Exception in database ","Exception",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

